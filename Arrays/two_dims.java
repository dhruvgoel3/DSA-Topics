//  Ways of initializing a 2D array in Java
public class two_dims {
    public static void main(String[] args) {
        int[][] Array = new int[3][3];
        int[][] MyArray = { { 1, 2, 3 }, { 4, 5, 6 }, { 7, 8, 9 } };

        System.out.println(MyArray[0][0]); // 1
        System.out.println(MyArray[0][1]); // 1
        System.out.println(MyArray[0][2]);

        System.out.println(MyArray[1][0]);// 1
        System.out.println(MyArray[1][1]);// 1
        System.out.println(MyArray[1][2]);// 1
    }

}
